#!/usr/bin/env node

/**
 * PrepGenie - Bulk Question Import Script
 *
 * This script imports verified questions from CSV/JSON files into the question bank.
 *
 * Usage:
 *   node scripts/import-questions.js <input-file.csv|.json>
 *
 * Example:
 *   node scripts/import-questions.js questions/jee-physics-2024.csv
 */

const fs = require('fs');
const path = require('path');

// CSV parser (simple implementation)
function parseCSV(content) {
  const lines = content.split('\n').filter(line => line.trim());
  const headers = lines[0].split(',').map(h => h.trim());

  const questions = [];
  for (let i = 1; i < lines.length; i++) {
    const values = parseCSVLine(lines[i]);
    if (values.length !== headers.length) {
      console.warn(`⚠️  Row ${i + 1} has ${values.length} columns, expected ${headers.length}. Skipping.`);
      continue;
    }

    const question = {};
    headers.forEach((header, index) => {
      question[header] = values[index].trim();
    });
    questions.push(question);
  }

  return questions;
}

// Parse CSV line handling quoted strings
function parseCSVLine(line) {
  const values = [];
  let current = '';
  let inQuotes = false;

  for (let i = 0; i < line.length; i++) {
    const char = line[i];

    if (char === '"') {
      inQuotes = !inQuotes;
    } else if (char === ',' && !inQuotes) {
      values.push(current);
      current = '';
    } else {
      current += char;
    }
  }
  values.push(current);

  return values;
}

// Validate question format
function validateQuestion(q, lineNum) {
  const errors = [];

  if (!q.question || q.question.trim().length < 10) {
    errors.push(`Row ${lineNum}: Question text too short or missing`);
  }

  if (!q.option_a || !q.option_b || !q.option_c || !q.option_d) {
    errors.push(`Row ${lineNum}: All 4 options (option_a, option_b, option_c, option_d) are required`);
  }

  const correctAnswer = parseInt(q.correct_answer);
  if (isNaN(correctAnswer) || correctAnswer < 0 || correctAnswer > 3) {
    errors.push(`Row ${lineNum}: correct_answer must be 0, 1, 2, or 3 (got: ${q.correct_answer})`);
  }

  if (!q.explanation || q.explanation.trim().length < 10) {
    errors.push(`Row ${lineNum}: Explanation too short or missing`);
  }

  if (!['easy', 'medium', 'hard'].includes(q.difficulty?.toLowerCase())) {
    errors.push(`Row ${lineNum}: difficulty must be easy, medium, or hard (got: ${q.difficulty})`);
  }

  if (!q.exam_id) {
    errors.push(`Row ${lineNum}: exam_id is required (e.g., jee-main, neet-ug, upsc-cse)`);
  }

  if (!q.subject_id) {
    errors.push(`Row ${lineNum}: subject_id is required (e.g., jee-physics, neet-biology)`);
  }

  if (!q.topic) {
    errors.push(`Row ${lineNum}: topic is required (e.g., Mechanics, Thermodynamics)`);
  }

  return errors;
}

// Convert to TypeScript format
function convertToTypeScript(questions, arrayName) {
  let ts = `// Auto-generated by import-questions.js\n`;
  ts += `// Generated: ${new Date().toISOString()}\n\n`;
  ts += `const ${arrayName}: BankQuestion[] = [\n`;

  questions.forEach((q, idx) => {
    ts += `  {\n`;
    ts += `    question: ${JSON.stringify(q.question)},\n`;
    ts += `    options: [\n`;
    ts += `      ${JSON.stringify(q.option_a)},\n`;
    ts += `      ${JSON.stringify(q.option_b)},\n`;
    ts += `      ${JSON.stringify(q.option_c)},\n`;
    ts += `      ${JSON.stringify(q.option_d)},\n`;
    ts += `    ],\n`;
    ts += `    correctAnswer: ${q.correct_answer},\n`;
    ts += `    explanation: ${JSON.stringify(q.explanation)},\n`;
    ts += `    difficulty: "${q.difficulty.toLowerCase()}",\n`;
    ts += `    source: "verified",\n`;

    if (q.year) {
      ts += `    year: ${JSON.stringify(q.year)},\n`;
    }

    if (q.source_detail) {
      ts += `    sourceDetail: ${JSON.stringify(q.source_detail)},\n`;
    }

    ts += `  }${idx < questions.length - 1 ? ',' : ''}\n`;
  });

  ts += `];\n\n`;
  ts += `export { ${arrayName} };\n`;

  return ts;
}

// Main function
function main() {
  const args = process.argv.slice(2);

  if (args.length === 0) {
    console.log(`
╔═══════════════════════════════════════════════════════════════╗
║          PrepGenie - Bulk Question Import Script              ║
╚═══════════════════════════════════════════════════════════════╝

Usage:
  node scripts/import-questions.js <input-file>

Supported formats:
  - CSV (.csv)
  - JSON (.json)

CSV Format (comma-separated):
  question,option_a,option_b,option_c,option_d,correct_answer,explanation,difficulty,exam_id,subject_id,topic,year,source_detail

  Fields:
    - question:        Question text (required)
    - option_a/b/c/d:  Four options (required)
    - correct_answer:  0, 1, 2, or 3 (required)
    - explanation:     Detailed explanation (required)
    - difficulty:      easy, medium, or hard (required)
    - exam_id:         e.g., jee-main, neet-ug (required)
    - subject_id:      e.g., jee-physics, neet-biology (required)
    - topic:           e.g., Mechanics, Thermodynamics (required)
    - year:            e.g., JEE Main 2024 (optional)
    - source_detail:   e.g., NCERT Class 12 Ch 1 (optional)

JSON Format:
  [
    {
      "question": "...",
      "option_a": "...",
      "option_b": "...",
      "option_c": "...",
      "option_d": "...",
      "correct_answer": 0,
      "explanation": "...",
      "difficulty": "medium",
      "exam_id": "jee-main",
      "subject_id": "jee-physics",
      "topic": "Mechanics",
      "year": "JEE Main 2024",
      "source_detail": "Shift 1"
    }
  ]

Examples:
  node scripts/import-questions.js questions/jee-physics-2024.csv
  node scripts/import-questions.js questions/neet-biology.json

Output:
  - Validates all questions
  - Generates TypeScript code in .agents/artifacts/
  - Groups questions by exam_id + subject_id + topic
  - Ready to copy-paste into src/lib/question-bank.ts
`);
    process.exit(0);
  }

  const inputFile = args[0];

  if (!fs.existsSync(inputFile)) {
    console.error(`❌ Error: File not found: ${inputFile}`);
    process.exit(1);
  }

  console.log(`\n📂 Reading file: ${inputFile}`);

  const ext = path.extname(inputFile).toLowerCase();
  const content = fs.readFileSync(inputFile, 'utf8');

  let rawQuestions = [];

  if (ext === '.csv') {
    console.log(`📊 Parsing CSV...`);
    rawQuestions = parseCSV(content);
  } else if (ext === '.json') {
    console.log(`📊 Parsing JSON...`);
    try {
      rawQuestions = JSON.parse(content);
    } catch (err) {
      console.error(`❌ Invalid JSON: ${err.message}`);
      process.exit(1);
    }
  } else {
    console.error(`❌ Unsupported file format: ${ext}`);
    console.error(`   Supported: .csv, .json`);
    process.exit(1);
  }

  console.log(`✅ Found ${rawQuestions.length} questions\n`);

  // Validate all questions
  console.log(`🔍 Validating questions...`);
  let allErrors = [];
  const validQuestions = [];

  rawQuestions.forEach((q, idx) => {
    const errors = validateQuestion(q, idx + 2); // +2 because of header and 0-index
    if (errors.length > 0) {
      allErrors.push(...errors);
    } else {
      validQuestions.push(q);
    }
  });

  if (allErrors.length > 0) {
    console.error(`\n❌ Validation errors found:\n`);
    allErrors.forEach(err => console.error(`   ${err}`));
    console.error(`\n❌ ${allErrors.length} error(s) found. Please fix and try again.\n`);
    process.exit(1);
  }

  console.log(`✅ All questions validated successfully!\n`);

  // Group questions by exam_id + subject_id + topic
  const groups = {};

  validQuestions.forEach(q => {
    const key = `${q.exam_id}__${q.subject_id}__${q.topic}`;
    if (!groups[key]) {
      groups[key] = {
        exam_id: q.exam_id,
        subject_id: q.subject_id,
        topic: q.topic,
        questions: []
      };
    }
    groups[key].questions.push(q);
  });

  console.log(`📦 Grouped into ${Object.keys(groups).length} arrays:\n`);

  // Generate TypeScript for each group
  const outputDir = '.agents/artifacts/imported-questions';
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  let allOutput = '';

  Object.values(groups).forEach(group => {
    const arrayName = `${group.exam_id.replace(/-/g, '_')}_${group.subject_id.replace(/-/g, '_')}_${group.topic.replace(/[^a-zA-Z0-9]/g, '_')}`;
    const tsCode = convertToTypeScript(group.questions, arrayName);

    const filename = `${arrayName}.ts`;
    const filepath = path.join(outputDir, filename);
    fs.writeFileSync(filepath, tsCode);

    console.log(`   ✅ ${group.exam_id} → ${group.subject_id} → ${group.topic}: ${group.questions.length} questions`);
    console.log(`      Saved to: ${filepath}\n`);

    allOutput += tsCode + '\n';
  });

  // Save combined file
  const combinedFile = path.join(outputDir, '_all_combined.ts');
  fs.writeFileSync(combinedFile, allOutput);

  console.log(`\n✨ Success! ${validQuestions.length} questions imported.`);
  console.log(`\n📁 Output files:`);
  console.log(`   Individual: ${outputDir}/`);
  console.log(`   Combined:   ${combinedFile}`);
  console.log(`\n📝 Next steps:`);
  console.log(`   1. Review the generated TypeScript code`);
  console.log(`   2. Copy the arrays into src/lib/question-bank.ts`);
  console.log(`   3. Update the export mappings in question-bank.ts`);
  console.log(`   4. Test with: npm run build\n`);
}

// Run
try {
  main();
} catch (err) {
  console.error(`\n❌ Unexpected error: ${err.message}`);
  console.error(err.stack);
  process.exit(1);
}
